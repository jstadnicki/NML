namespace NML.Controls
{
    /// <summary>
    /// Interaction logic for LinkDisplay.xaml
    /// </summary>
    public partial class LinkDisplay : UserControl
    {
        public LinkDisplay()
        {
            InitializeComponent();
        }

        public string Text
        {
            get { return (string)GetValue(TextProperty); }
            set { SetValue(TextProperty, value); }
        }

        // Using a DependencyProperty as the backing store for Text.  This enables animation, styling, binding, etc...
        public static readonly DependencyProperty TextProperty =
            DependencyProperty.Register("Text", typeof(string), typeof(LinkDisplay), new PropertyMetadata(""));

        public string Url
        {
            get { return (string)GetValue(UrlProperty); }
            set { SetValue(UrlProperty, value); }
        }

        // Using a DependencyProperty as the backing store for Url.  This enables animation, styling, binding, etc...
        public static readonly DependencyProperty UrlProperty =
            DependencyProperty.Register("Url", typeof(string), typeof(LinkDisplay), new PropertyMetadata(""));

        private void RootControl_MouseDown(object sender, MouseButtonEventArgs e)
        {
            Process p = new Process();
            p.StartInfo.FileName = Url;
            p.Start();
        }                
    }
}
